package DSA.Arrays.Algos;

public class Swap {
    public static void main(String[] args) {
        int number = 78;
        int anotherNumber = 10;
        int lastNum = number;
        number = anotherNumber;
        anotherNumber = lastNum;
        System.out.println(anotherNumber);
        System.out.println(number);
    }
}
